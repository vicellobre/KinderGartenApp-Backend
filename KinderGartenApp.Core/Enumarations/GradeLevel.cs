namespace KinderGartenApp.Core.Enumarations;

/// <summary>
/// Enum que representa los niveles educativos o grados en el jardín de niños.
/// </summary>
public enum GradeLevel
{
    /// <summary>
    /// Nivel Pre-Kínder, generalmente el primer nivel para los estudiantes más jóvenes.
    /// </summary>
    PreKinder,

    /// <summary>
    /// Primer nivel de Kínder.
    /// </summary>
    Kinder1,

    /// <summary>
    /// Segundo nivel de Kínder.
    /// </summary>
    Kinder2,

    /// <summary>
    /// Tercer nivel de Kínder, el último nivel antes de la educación primaria.
    /// </summary>
    Kinder3
}
